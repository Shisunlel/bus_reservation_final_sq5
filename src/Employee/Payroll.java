    private void refreshTable() {
        var payrolls = PayrollClass.getAllPayrolls();
        pTable = (DefaultTableModel) payrollTable.getModel();
        pTable.setRowCount(0);
        for (var payroll : payrolls) {
            Object[] data = new Object[9];
            data[0] = payroll.get("id");
            data[1] = String.format("%s %s", Helper.toUpperCase(payroll.get("last_name").toString()), Helper.toUpperCase(payroll.get("first_name").toString()));
            data[2] = payroll.get("date");
            data[3] = payroll.get("amount");
            data[4] = payroll.get("bonus");
            data[5] = payroll.get("deduction");
            data[6] = payroll.get("exchange_rate");
            data[7] = payroll.get("total_usd");
            data[8] = payroll.get("total_khr");
            pTable.addRow(data);
        }
    }

