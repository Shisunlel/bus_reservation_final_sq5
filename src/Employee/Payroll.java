    private void payrollTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_payrollTableMouseClicked
        int row = payrollTable.getSelectedRow();
        id = payrollTable.getValueAt(row, 0).toString();
        int index = 0;
        for (var staff : staffs) {
            String name = staff.get("first_name").toString() + ' ' + staff.get("last_name").toString();
            if (name.toLowerCase().equals(payrollTable.getValueAt(row, 1).toString().toLowerCase())) {
                cbStaff.setSelectedIndex(index);
            }
            index++;
        }
        txtAmount.setText(payrollTable.getValueAt(row, 3).toString());
        txtBonus.setText(payrollTable.getValueAt(row, 4).toString());
        txtDeduction.setText(payrollTable.getValueAt(row, 5).toString());
        txtExchange.setText(payrollTable.getValueAt(row, 6).toString());
        LocalDate thisDate = LocalDate.parse(payrollTable.getValueAt(row, 2).toString());
        dDate.setDate(thisDate);
    }//GEN-LAST:event_payrollTableMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        String empId = ((ComboItem) cbStaff.getSelectedItem()).getValue();
        String date = dDate.getDate().toString();
        double amount = Double.parseDouble(txtAmount.getText());
        double bonus = Double.parseDouble(txtBonus.getText());
        double deduction = Double.parseDouble(txtDeduction.getText());
        int exchange_rate = Integer.parseInt(txtExchange.getText());
        double total_usd = amount + bonus - deduction;
        int total_khr = (int) Math.round(total_usd * exchange_rate);
        HashMap<String, String> message = PayrollClass.UpdatePayroll(id, empId, date, amount, bonus, deduction, exchange_rate, total_khr, total_usd);
        JOptionPane.showMessageDialog(this, message.get("message"));
        if ("1".equals(message.get("code"))) {
            refreshTable();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

