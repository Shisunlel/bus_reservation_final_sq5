    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String firstName = txtFirstName.getText();
        String lastName = txtLastName.getText();
        String phone = txtPhone.getText();
        HashMap<String, String> message = Passenger.AddPassenger(firstName, lastName, phone);
        JOptionPane.showMessageDialog(this, message.get("message"));
        if ("1".equals(message.get("code"))) {
            refreshTable();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        String firstName = txtFirstName.getText();
        String lastName = txtLastName.getText();
        String phone = txtPhone.getText();
        HashMap<String, String> message = Passenger.UpdatePassenger(Integer.parseInt(id), firstName, lastName, phone);
        JOptionPane.showMessageDialog(this, message.get("message"));
        if ("1".equals(message.get("code"))) {
            refreshTable();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void passengerTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_passengerTableMouseClicked
        int row = passengerTable.getSelectedRow();
        id = passengerTable.getValueAt(row, 0).toString();
        txtFirstName.setText(passengerTable.getValueAt(row, 1).toString());
        txtLastName.setText(passengerTable.getValueAt(row, 2).toString());
        txtPhone.setText(passengerTable.getValueAt(row, 3).toString());
    }//GEN-LAST:event_passengerTableMouseClicked


