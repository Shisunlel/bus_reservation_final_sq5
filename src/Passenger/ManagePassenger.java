    private void refreshTable() {
        var passengers = Passenger.getAllPassengers();
        pTable = (DefaultTableModel) passengerTable.getModel();
        pTable.setRowCount(0);
        for (var passenger : passengers) {
            Object[] data = new Object[4];
            data[0] = passenger.get("id");
            data[1] = passenger.get("first_name");
            data[2] = passenger.get("last_name");
            data[3] = passenger.get("mobile");
            pTable.addRow(data);
        }
    }
