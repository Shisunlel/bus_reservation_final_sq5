    private void vehicleTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_vehicleTableMouseClicked
        // TODO add your handling code here:
        int row = vehicleTable.getSelectedRow();
        id = Integer.parseInt(vTable.getValueAt(row, 0).toString());
        txtModel.setText(vTable.getValueAt(row, 1).toString());
        for (var vType : vehicleTypes) {
            if (vType.get("type").toString().equals(vTable.getValueAt(row, 2).toString())) {
                cbVType.setSelectedItem(new ComboItem(vType.get("type").toString(), vType.get("id").toString()));
            }
        }
        txtPlate.setText(vTable.getValueAt(row, 3).toString());
        txtSeat.setText(vTable.getValueAt(row, 4).toString());
        if (vTable.getValueAt(row, 5).toString().equals("Yes")) {
            activeY.setSelected(true);
        } else {
            activeN.setSelected(true);
        }
    }//GEN-LAST:event_vehicleTableMouseClicked


