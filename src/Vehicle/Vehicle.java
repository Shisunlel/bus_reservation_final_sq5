    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String model = txtModel.getText();
        String type = ((ComboItem) cbVType.getSelectedItem()).getValue();
        String plate = txtPlate.getText();
        int seat = Integer.parseInt(txtSeat.getText());
        String is_active = buttonGroup1.getSelection().getActionCommand();
        HashMap<String, String> message = VehicleClass.AddVehicle(type, plate, model, seat, is_active);
        JOptionPane.showMessageDialog(this, message.get("message"));
        if ("1".equals(message.get("code"))) {
            refreshTable();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

