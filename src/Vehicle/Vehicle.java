    private void refreshTable() {
        var vehicles = VehicleClass.getAllVehicles();
        vTable = (DefaultTableModel) vehicleTable.getModel();
        vTable.setRowCount(0);
        for (var vehicle : vehicles) {
            Object[] data = new Object[6];
            data[0] = vehicle.get("id");
            data[1] = vehicle.get("model");
            data[2] = vehicle.get("type");
            data[3] = vehicle.get("plate_no");
            data[4] = vehicle.get("total_seat");
            data[5] = vehicle.get("is_active").equals(true) ? "Yes" : "No";
            vTable.addRow(data);
        }
    }
    
