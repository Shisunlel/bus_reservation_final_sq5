    private void refreshTable() {
        var transactions = Finance.getTransactions();
        tTable = (DefaultTableModel) transactionTable.getModel();
        tTable.setRowCount(0);
        for (var transaction : transactions) {
            Object[] data = new Object[5];
            data[0] = transaction.get("id");
            data[1] = transaction.get("name");
            data[2] = transaction.get("amount");
            data[3] = transaction.get("description");
            data[4] = transaction.get("date");
            tTable.addRow(data);
        }
    }

