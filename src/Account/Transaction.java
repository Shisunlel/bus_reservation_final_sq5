    private void transactionTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_transactionTableMouseClicked
        int row = transactionTable.getSelectedRow();
        id = Integer.parseInt(transactionTable.getValueAt(row, 0).toString());
        txtAmount.setText(transactionTable.getValueAt(row, 2).toString());
        int i = 0;
        for (var account : accounts) {
            if (account.get("name").toString().equals(transactionTable.getValueAt(row, 1).toString())) {
                cbAccount.setSelectedIndex(i);
            }
            i++;
        }
        txtDesc.setText(transactionTable.getValueAt(row, 3).toString());
        LocalDate thisDate = LocalDate.parse(transactionTable.getValueAt(row, 4).toString());
        dDate.setDate(thisDate);
    }//GEN-LAST:event_transactionTableMouseClicked

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        int typeId = Integer.parseInt(((ComboItem) cbAccount.getSelectedItem()).getValue());
        double amount = Double.parseDouble(txtAmount.getText());
        String desc = txtDesc.getText();
        String date = dDate.getDate().toString();
        HashMap<String, String> message = Finance.AddTransaction(typeId, desc, amount, date);
        JOptionPane.showMessageDialog(this, message.get("message"));
        if ("1".equals(message.get("code"))) {
            refreshTable();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

