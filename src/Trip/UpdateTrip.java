    public UpdateTrip(Trip parent, Integer id, String vehicle_name, String driver_name, String route_name, String departure_datetime, String arrival_datetime, Integer status) {
        initComponents();
        setAlwaysOnTop(true);
        setResizable(false);
        setDefaultCloseOperation(DISPOSE_ON_CLOSE);

        this.parent = parent;
        uid = id;
        this.vehicle_name = vehicle_name;
        this.route_name = route_name;
        this.driver_name = driver_name;

        if (status == 0) {
            rdbtnInactive.setSelected(true);
        } else {
            rdbtnActive.setSelected(true);
        }
        String[] split = departure_datetime.split(" ");
        String d_date = split[0];
        String d_time = split[1];

        split = arrival_datetime.split(" ");
        String a_date = split[0];
        String a_time = split[1];

        TimePickerSettings timeSettings = new TimePickerSettings();
        timeSettings.use24HourClockFormat();
        departure = new DateTimePicker(null, timeSettings);
        arrival = new DateTimePicker(null, timeSettings);
        departure.getDatePicker().setDate(LocalDate.parse(d_date));
        departure.getTimePicker().setTime(LocalTime.parse(d_time));
        arrival.getDatePicker().setDate(LocalDate.parse(a_date));
        arrival.getTimePicker().setTime(LocalTime.parse(a_time));
        departure.setSize(new Dimension(235, 37));
        arrival.setSize(new Dimension(235, 37));
        jPanel11.add(departure);
        jPanel13.add(arrival);
    }

    public void setMapsForComboxBox(Map<String, Integer> drivers, Map<String, Integer> routes, Map<String, Integer> vehicles) {
        this.drivers = drivers;
        this.routes = routes;
        this.vehicles = vehicles;
        cbDriver.removeAllItems();
        cbVehicle.removeAllItems();
        cbRoute.removeAllItems();
        //load it
        for (String driver : drivers.keySet()) {
            cbDriver.addItem(driver);
        }
        for (String vehicle : vehicles.keySet()) {
            cbVehicle.addItem(vehicle);
        }
        for (String route : routes.keySet()) {
            cbRoute.addItem(route);
        }
        cbDriver.setSelectedItem(driver_name);
        cbVehicle.setSelectedItem(vehicle_name);
        cbRoute.setSelectedItem(route_name);
        System.out.println(cbRoute.getSelectedItem().toString());
        System.out.println(route_name);
    }

