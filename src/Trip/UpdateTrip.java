    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Integer status = 0;
        Integer vehicle_id = vehicles.get(cbVehicle.getSelectedItem());

        String d_date = departure.getDatePicker().getDate().format(DateTimeFormatter.ISO_LOCAL_DATE);
        String d_time = departure.getTimePicker().getText();
        String d_datetime = d_date + " " + d_time + ":00";
        Timestamp departureTimestamp = Timestamp.valueOf(d_datetime);

        String a_date = arrival.getDatePicker().getDate().format(DateTimeFormatter.ISO_LOCAL_DATE);
        String a_time = arrival.getTimePicker().getText();
        String a_datetime = a_date + " " + a_time + ":00";
        Timestamp arrivalTimestamp = Timestamp.valueOf(a_datetime);

        if (rdbtnActive.isSelected()) {
            status = 1;
        }
        try {
            Connection conn = DBCon.getConnection();
            String query = "UPDATE trip SET vehicle_id=?, route_id=?, driver_id=?, start_date=?, end_date=?,"
                    + " sold_ticket=0, total_income=0, status=? WHERE id=?";
            PreparedStatement prepSt = conn.prepareStatement(query);
            prepSt.setInt(1, vehicle_id);
            prepSt.setInt(2, routes.get(cbRoute.getSelectedItem()));
            prepSt.setInt(3, drivers.get(cbDriver.getSelectedItem()));
            prepSt.setTimestamp(4, departureTimestamp);
            prepSt.setTimestamp(5, arrivalTimestamp);
            prepSt.setInt(6, status);
            prepSt.setInt(7, uid);
            prepSt.executeUpdate();
        } catch (SQLException e) {
            e.printStackTrace(System.err);
        }
        parent.reloadTable();
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

