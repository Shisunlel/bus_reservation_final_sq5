    private void addPermissionTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addPermissionTableMouseClicked
        int row = addPermissionTable.getSelectedRow();
        id = addPermissionTable.getValueAt(row, 0).toString();
        txtName.setText(addPermissionTable.getValueAt(row, 1).toString());
        String read = addPermissionTable.getValueAt(row, 2).toString();
        String create = addPermissionTable.getValueAt(row, 3).toString();
        String update = addPermissionTable.getValueAt(row, 4).toString();
        String delete = addPermissionTable.getValueAt(row, 5).toString();
        cbRead.setSelectedItem(read);
        cbCreate.setSelectedItem(create);
        cbUpdate.setSelectedItem(update);
        cbDelete.setSelectedItem(delete);
    }//GEN-LAST:event_addPermissionTableMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int read = cbRead.getSelectedIndex() == 0 ? 1 : 0;
        int create = cbCreate.getSelectedIndex() == 0 ? 1 : 0;
        int update = cbUpdate.getSelectedIndex() == 0 ? 1 : 0;
        int delete = cbDelete.getSelectedIndex() == 0 ? 1 : 0;
        HashMap<String, String> message = Position.UpdatePermission(Integer.parseInt(id), read, create, update, delete);
        JOptionPane.showMessageDialog(this, message.get("message"));
        if ("1".equals(message.get("code"))) {
            refreshTable();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed


