    private void refreshTable() {
        var permissions = Position.getAllPermissions();
        addPTable = (DefaultTableModel) addPermissionTable.getModel();
        addPTable.setRowCount(0);
        for (var permission : permissions) {
            Object[] data = new Object[6];
            data[0] = permission.get("id");
            data[1] = permission.get("name");
            data[2] = permission.get("read").equals(true) ? "Yes" : "No";
            data[3] = permission.get("create").equals(true) ? "Yes" : "No";
            data[4] = permission.get("update").equals(true) ? "Yes" : "No";
            data[5] = permission.get("delete").equals(true) ? "Yes" : "No";
            addPTable.addRow(data);
        }
    }

