    private void assignPermissionTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_assignPermissionTableMouseClicked
        int row = assignPermissionTable.getSelectedRow();
        int i = 0;
        int j = 0;
        for (var position : positions) {
            if (position.get("name").toString().equals(assignPerTable.getValueAt(row, 0).toString())) {
                position_id = Integer.parseInt(position.get("id").toString());
                cbPosition.setSelectedIndex(i);
            }
            i++;
        }
        for (var permission : permissions) {
            if (permission.get("name").toString().equals(assignPerTable.getValueAt(row, 1).toString())) {
                permission_id = Integer.parseInt(permission.get("id").toString());
                cbPermission.setSelectedIndex(j);
            }
            j++;
        }
    }//GEN-LAST:event_assignPermissionTableMouseClicked

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String position = ((ComboItem) cbPosition.getSelectedItem()).getValue();
        String permission = ((ComboItem) cbPermission.getSelectedItem()).getValue();
        HashMap<String, String> message = Position.AddPermissionPosition(Integer.parseInt(permission), Integer.parseInt(position));
        JOptionPane.showMessageDialog(this, message.get("message"));
        if ("1".equals(message.get("code"))) {
            refreshTable();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String position = ((ComboItem) cbPosition.getSelectedItem()).getValue();
        String permission = ((ComboItem) cbPermission.getSelectedItem()).getValue();
        HashMap<String, String> message = Position.UpdatePermissionPosition(permission_id, position_id, Integer.parseInt(permission), Integer.parseInt(position));
        JOptionPane.showMessageDialog(this, message.get("message"));
        if ("1".equals(message.get("code"))) {
            refreshTable();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed


