    private void refreshTable() {
        var permissionsPositions = Position.getAllPermissionPosition();
        assignPerTable = (DefaultTableModel) assignPermissionTable.getModel();
        assignPerTable.setRowCount(0);
        for (var perPos : permissionsPositions) {
            Object[] data = new Object[2];
            data[0] = perPos.get("position_name");
            data[1] = perPos.get("permission_name");
            assignPerTable.addRow(data);
        }
    }

