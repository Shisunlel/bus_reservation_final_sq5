    public AssignPermission() {
        initComponents();
        positions = Position.getAllPositions();
        permissions = Position.getAllPermissions();
        for (var position : positions) {
            cbPosition.addItem(new ComboItem(position.get("name").toString(), position.get("id").toString()));
        }
        for (var permission : permissions) {
            cbPermission.addItem(new ComboItem(permission.get("name").toString(), permission.get("id").toString()));
        }
        refreshTable();
    }

